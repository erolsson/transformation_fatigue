        SUBROUTINE GETPARTINFOC(NAME, LOCNUM, JTYPE, USER_NUMBER, JRCD)
        USE ISO_C_BINDING
        CHARACTER(kind=C_CHAR), dimension(*), intent(in) :: NAME
        EXTERNAL GETPARTINFO
        CALL GETPARTINFO(LOCNUM, JTYPE, NAME, USER_NUMBER, JRCD)
        END

